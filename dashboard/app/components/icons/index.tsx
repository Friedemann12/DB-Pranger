export function WeatherIcon({ code, size = 24, className = "" }: { code: number } & IconProps) {
  if (code === 0) return <SunIcon size={size} className={className} />;
  if (code <= 3) return <CloudSunIcon size={size} className={className} />;
  if (code <= 48) return <CloudFogIcon size={size} className={className} />;
  if (code <= 67) return <CloudRainIcon size={size} className={className} />;
  if (code <= 86) return <CloudSnowIcon size={size} className={className} />;
  if (code >= 95) return <ThunderstormIcon size={size} className={className} />;
  return <CloudIcon size={size} className={className} />;
}

export function VehicleIcon({ type, size = 24, className = "" }: { type: string | null } & IconProps) {
  const vehicleType = type?.toUpperCase() || "";
  if (vehicleType.includes("U_BAHN") || vehicleType.includes("S_BAHN") || vehicleType === "TRAIN") {
    return <TrainIcon size={size} className={className} />;
  }
  if (vehicleType.includes("FERRY")) {
    return <FerryIcon size={size} className={className} />;
  }
  return <BusIcon size={size} className={className} />;
}

